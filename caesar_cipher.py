alphabet = ['a', 'b', 'c', 'd', 'e', 'f', 'g', 'h', 'i', 'j', 'k', 'l', 'm', 'n', 'o', 'p', 'q', 'r', 's', 't', 'u',
            'v', 'w', 'x', 'y', 'z', 'a', 'b', 'c', 'd', 'e', 'f', 'g', 'h', 'i', 'j', 'k', 'l', 'm', 'n', 'o', 'p',
            'q', 'r', 's', 't', 'u', 'v', 'w', 'x', 'y', 'z']


def caesar(direct, text_to_change, shift_amount):
    new_text = ""
    if direct.lower() == "decode":
        shift_amount *= -1
    for character in text_to_change:
        if character in alphabet:
            alphabet_index = alphabet.index(character)
            new_index = alphabet_index + shift_amount
            new_text += alphabet[new_index]
        else:
            new_text += character
    print(f"The {direct}d text is {new_text}")


